<h1>Om webbplatsen</h1>

<p>Detta projekt avslutar och examinerar kursen <em>ramverk1</em> (HT20, lp2) som ingår i programmet Webbprogrammering (120 hp) på BTH.</p>
<p>I enighet med <a href="https://dbwebb.se/kurser/ramverk1-v2/kmom10#upplagg" target="_blank">projektspecifikationen</a> är webbplatsen ett Q&A-forum där besökarna får läsa schackrelaterade frågor, svar och kommentarer medan den som är inloggad även får skapa nya texter och betygsätta befintligt innehåll.</p>
<p>Frågorna är kopplade till taggar och varje registrerad användare har ett reputation-score som räknas ut utifrån användarens bidrag till forumet (såsom värdefulla svar och intressanta frågor).</p>
<p>Webbplatsen är byggd med hjälp av databasen SQLITE och PHP-ramverket Anax. Du hittar källkoden till projektet på <a href="https://github.com/ahonson/projekt-ramverk1" target="_blank">Github</a>. Githubrepot är integrerat i en CI-kedja som omfattar Travis och Scrutinizer. Följ README-filens instruktioner för att ladda ner din egen installation av webbplatsen.</p>
